use specta_typescript as ts;
use std::path::Path;
use vloc_engine::error::EngineError;
use vloc_engine::models::{Coordinates, Route, SpoofingState, EndOfRouteBehavior};

fn main() {
    let out_dir = Path::new("../../packages/vloc-api-bindings/src");
    let out_file = out_dir.join("bindings.ts");

    println!("Generating bindings to: {:?}", out_file);

    let mut bindings = String::from("// Auto-generated by vloc-engine. Do not edit.\n\n");

    // Export types using the specta_typescript crate directly
    bindings.push_str(&ts::export::<Coordinates>(&Default::default()).unwrap());
    bindings.push_str("\n");
    bindings.push_str(&ts::export::<Route>(&Default::default()).unwrap());
    bindings.push_str("\n");
    bindings.push_str(&ts::export::<EndOfRouteBehavior>(&Default::default()).unwrap());
    bindings.push_str("\n");
    bindings.push_str(&ts::export::<SpoofingState>(&Default::default()).unwrap());
    bindings.push_str("\n");
    bindings.push_str(&ts::export::<EngineError>(&Default::default()).unwrap());
    bindings.push_str("\n");

    // Manually add the IPC function signatures
    bindings.push_str(
        r#"
import { invoke } from "@tauri-apps/api/core";

/**
 * Standard IPC Commands available in the VLoc Engine.
 */
export const commands = {
  /** Starts the spoofing simulation. */
  async start_route(route: Route, speed_kmh: number, inaccuracy_meters: number): Promise<void> {
    return await invoke("start_route", { route, speedKmh: speed_kmh, inaccuracyMeters: inaccuracy_meters });
  },
  /** Stops the active spoofing simulation. */
  async stop_route(): Promise<void> {
    return await invoke("stop_route");
  },
  /** Retrieves the current synchronization state from the engine. */
  async get_current_state(): Promise<SpoofingState> {
    return await invoke("get_current_state");
  }
};
"#,
    );

    std::fs::write(out_file, bindings).expect("Failed to write bindings.ts");
    println!("Success!");
}
